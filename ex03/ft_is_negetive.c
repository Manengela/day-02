#include <unistd.h>

void ft_putchar(char i)

{
	write(1, &i, 1);

}

void ft_is_negetive(int i)

{
	if  (i >= 0)
	{
	ft_putchar('p');
	}
	while (i <= 0)
	{
	ft_putchar('n');
	}


}

